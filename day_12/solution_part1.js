let arrHeight;
let allWays = [];

function findWayToLocation (data) {
    arrHeight = data.split('\n');

    for (let l = 0; l < arrHeight.length; l++) {
        for (let i = 0; i <  arrHeight[l].length; i++) {
            if (arrHeight[l][i] == 'S') {
                findAllWay([[l, i]], l, i);
                break;
            }
        }
    };


    return Math.min(...allWays)
}

function findAllWay(currWay, y, x) {
    if (arrHeight[y][x] == 'E') {
        allWays.push(currWay.length - 1);
    } else {
        checkAnotherWays(currWay)
    }
}


function checkAnotherWays(way) {
    let [y, x] = way[way.length - 1];
    let letter = arrHeight[y][x];
    if (letter == 'S') letter = 'a'

    if (isPossibleIndex(way, y+1, x) && isRightLetter(y+1, x, letter)){
        let newWay = [...way,[y+1, x]];
        findAllWay(newWay, y+1, x);
    }

    if (isPossibleIndex(way, y, x+1) && isRightLetter(y, x+1, letter)) {
        let newWay = [...way,[y, x+1]];
        findAllWay(newWay, y, x+1);
    }

    if (isPossibleIndex(way, y-1, x) && isRightLetter(y-1, x, letter)) {
        let newWay = [...way,[y-1, x]];;
        findAllWay(newWay, y-1, x);
    }

    if (isPossibleIndex(way, y, x-1) && isRightLetter(y, x-1, letter)) {
        let newWay = [...way,[y, x-1]];;
        findAllWay(newWay, y, x-1);
    }
}

function isPossibleIndex(way, y, x) {
    if (y < 0 || y >= arrHeight.length) return false;
    if (x < 0 || x >= arrHeight[0].length) return false;

    for (let i = 0; i < way.length; i++) {
        let [elemY, elemX] = way[i];
        if (elemY == y && elemX == x) {
            return false;
        }
    }

    return true;
}

function isRightLetter(y, x, currLetter) {
    let charCodeFirst = arrHeight[y][x].charCodeAt(0);
    let charCodeSecond = currLetter.charCodeAt(0);

    if (arrHeight[y][x] == 'E') {
        charCodeFirst = 'z'.charCodeAt(0);
    }

    if ((charCodeFirst - charCodeSecond) == 1 || charCodeFirst == charCodeSecond) {
        return true;
    }

    return false;
}

